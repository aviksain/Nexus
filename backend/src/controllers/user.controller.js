const getUserChannelProfile = asyncHandler(async (req, res) => {
  const { username } = req.params;

  if (!username?.trim()) {
    throw new ApiError(400, "username is missing");
  }

  // aggregate function returns a array of objects
  const channel = await User.aggregate([
    {
      $match: {
        username: username?.toLowerCase(),
      },
    },
    {
      $lookup: {
        from: "subscriptions",
        localField: "_id",
        foreignField: "channel",
        as: "subscribers",
      },
    },
    {
      $lookup: {
        from: "subscriptions",
        localField: "_id",
        foreignField: "subscriber",
        as: "subscribedTo",
      },
    },
    {
      $addFields: {
        subscribersCount: {
          $size: "$subscribers",
        },
        channelsSubscribedToCount: {
          $size: "$subscribedTo",
        },
        isSubscribed: {
          $cond: {
            if: { $in: [req.user?._id, "$subscribers.subscriber"] },
            then: true,
            else: false,
          },
        },
      },
    },
    {
      $project: {
        fullname: 1,
        username: 1,
        subscribersCount: 1,
        channelsSubscribedToCount: 1,
        isSubscribed: 1,
        avatar: 1,
        coverImage: 1,
        email: 1,
      },
    },
  ]);

  if (!channel?.length) {
    throw new ApiError(404, "channel does not exists");
  }

  return res
    .status(200)
    .json(
      new ApiResponse(200, channel[0], "User channel fetched successfully")
    );
});

const getWatchHistory = asyncHandler(async (req, res) => {
  /*
    when we access _id through mongoose we get a string like this "661179c5f7a9ab95be51236f" but this not mongodb id, if we want mongodb objectid we have to do something like this new mongoose.Types.ObjectId(req.user._id)
  */

  /*
    So we will get the user object as 
    user = [
      {
        username,
        email,
        fullname,
        avatar,
        coverImage,
        watchHistory: [
          videoFile,
          thumbnail,
          title,
          description,
          duration,
          views,
          isPublished,
          owner: {
            fullname,
            username,
            avatar,
          }
        ],
        password,
        refreshToken,
      }
    ]
  */

  const user = await User.aggregate([
    {
      $match: {
        _id: new mongoose.Types.ObjectId(req.user._id),
      },
    },
    {
      /* MongoDB will match each element in the array with documents in the from collection (specified by foreignField). This means that for each input document, the $lookup stage will perform a match for each value in the array.
       */
      $lookup: {
        from: "videos",
        localField: "watchHistory",
        foreignField: "_id",
        as: "watchHistory", // will get the array
        pipeline: [
          {
            /*
              Extracting the owner details by the _id
            */
            $lookup: {
              from: "users",
              localField: "owner",
              foreignField: "_id",
              as: "owner",
              pipeline: [
                {
                  $project: {
                    fullname: 1,
                    username: 1,
                    avatar: 1,
                  },
                },
              ],
            },
          },
          {
            $addFields: {
              /*
              in the previous stage we will get array of objects
              so we overriding the owner field and returning as object
              first we returing as owner[{}]
              now we are returning as owner{}
              so we can access it as owner.fullname
              */
              owner: {
                $first: "$owner",
              },
            },
          },
        ],
      },
    },
  ]);

  return res
    .status(200)
    .json(
      new ApiResponse(
        200,
        user[0].watchHistory,
        "Watch history fetched successfully"
      )
    );
});

